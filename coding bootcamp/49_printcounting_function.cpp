#include<bits/stdc++.h>
using namespace std; 


// void keyword specifies that function doesn't return a value
void counting (int num) {
    for(int i = 1; i <= num; i ++) {
        cout<<i<<" "<<endl;
    }



}




int main () {
    int number; 
    cin>>number; 

    counting(number); 
    return 0;
}