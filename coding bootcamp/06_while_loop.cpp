// problem - print numbers from 1 to n  

// note - we use while(_) this loop will run till the statement inside it is true
#include<iostream> 
using namespace std; 
int main() {
    int n; 
    cin>>n; 
    int i = 1;


    while(i<=n) {
        cout<<i<<" ";
        i = i+1;
// we use i = i + 1 for checking the next number after i 
    }





    return 0;

}






